// FoxArt returns styled ASCII art based on context
func FoxArt(foxType string) string {
	var art string
	var style lipgloss.Style

	switch foxType {
	case "small":
		art = SmallFox
		style = AccentStyle
	case "sleepy":
		art = SleepyFox
		style = AccentStyle.MarginBottom(1)
	case "ailloy":
		art = AilloyFox
		style = AccentStyle.MarginBottom(1)
	case "inquisitive":
		art = InquisitiveFox
		style = AccentStyle.MarginBottom(1)
	case "celebration":
		art = CelebrationFox
		style = SuccessStyle.MarginBottom(1)
	case "thinking":
		art = ThinkingFox
		style = InfoStyle.MarginBottom(1)
	case "working":
		art = WorkingFox
		style = AccentStyle.MarginBottom(1)
	default:
		art = SmallFox
		style = AccentStyle
	}

	return style.Render(art)
}

// WelcomeChrome is the title/subtitle/version block of the welcome banner,
// without the fox. Useful when the fox is rendered separately so it can be
// animated.
func WelcomeChrome(ver string) string {
	title := lipgloss.NewStyle().
		Background(Primary1).
		Foreground(White).
		Bold(true).
		Padding(1, 3).
		MarginTop(1).
		MarginBottom(1).
		Render("🧠 AILLOY")

	subtitle := SubtleStyle.Render("AI-powered development workflows")

	version := InfoStyle.Render(ver)

	return lipgloss.JoinVertical(
		lipgloss.Center,
		title,
		subtitle,
		"",
		version,
	)
}

// Welcome banner with ailloy fox matching the main logo
func WelcomeBanner(ver string) string {
	return lipgloss.JoinVertical(
		lipgloss.Center,
		FoxArt("ailloy"),
		WelcomeChrome(ver),
	)
}

